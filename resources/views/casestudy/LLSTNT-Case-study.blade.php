                information and support services.</p>
            <p>TNT Canada desired to redesign TNT Canada CMS using Drupal 7 and integrate existing applications and
                Database to fetch Events Data etc. from central DB.We built the new web based application on Apache, PHP
                and MySQL using the Drupal content management system. The complete website is bilingual, operating
                system independent and accessible with any standard compliant browser. Communication of data is through
                APIs or Web services in XML and then Drupal manages the data. All the content entry and storage
                operations are administered by one admin area.</p>
            <p>Content Approval Workflows, Search Engine Compliance, Integration with Video Ad Networks, YouTube and
