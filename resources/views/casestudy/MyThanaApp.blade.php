              <h3>Overview</h3>
              <p>Civic responsibility! The most common and basic duty that is required of each and every citizen. With
                  the growing influence of Smart Phones and Technology in general My Thana app initiative provides a
                  highly unique collaborative platform connecting common citizens with law enforcement agencies around
                  them, thus helping in building a harmonious, crime free environment. </p>
              <p>As an initiative taken by MP police, this app will help the citizens of Indore (and other regional
                  cities in due course) by connecting them with law enforcement in any instance of crime or emergency.
              </p>

              <h3>The First Step</h3>

              <p>In conformance with MP Police guidelines, the first step we at Digiprima took was to ensure seamless
                  user experience. We achieved this by ensure simple and easy user on-boarding process. We started with
                  first understanding end user needs. Our team in lieu with actual law enforcement personnel conducted
                  comprehensive workshops and ground surveys to fully grasp the needs of our end users. Our aim was to
                  figure out the main pain points of the end users. Armed with these quantifiable findings, allowed us
                  to prepare:</p>
              <ul class="new-sec-list">
                  <li>End User Persona.</li>
                  <li>User Stories and Epics</li>
                  <li>User On-Boarding Process </li>
              </ul>
              <h3>The Plan</h3>
              <p>Once we identified “HOW”, we explored “WHAT ELSE”. The biggest challenge we faced was to check on the
                  credibility of information being passed on to the application by end users. </p>
              <p>When dealing with something this sensitive, we had to make sure to provide a way to assign
                  accountability for end-users as well. In order to do so, we simply integrated the existing National
                  Population Register (UIDAI DB) within the application. </p>
              <p>This integration allowed us to verify all end-users (User KYC) instantaneously while also ensuring
                  their DATA and identify is kept safe and secure</p>
              <h3>The Execution</h3>
              <p>The overall application was broken down into various modules or services it offered. Along with our
                  agency (Law Enforcement) partners, we identified the following features that the app would offer to
                  its end users: </p>
              <ul class="new-sec-list">
                  <li><b> SOS Message:</b>The app will allow users to set up multiple contacts (as in emergency
                      contacts) to whom an instant SOS message will be sent by triggering certain actions within the
                      device (Phone Shake and Power Button Press). The SOS message will be accompanied with real time
                      location tracking of the user. The same message will be sent to the closet law enforcement station
                      as well. </li>
                  <li><b>Report an Incident:</b>This feature will allow users to report any and all criminal incidents
                      to agencies. Users will have the option to report such activites anonymously as well. With its
                      built in form, this feature will allow users to submit complaints in text format with the option
                      to attach images, videos and other relevant media. </li>
                  <li><b> Call Police: </b>This feature when activated will connect the user directly with the police
                      HQ. Users will also have the option to initiate Video Calling. </li>
                  <li><b>Theft Reporting: </b>Users now will be able to file theft and lost reports remotely via this
                      app as against going down to a station or out-post. Each complaint filed will be accompanied with
                      an acknowledgement and case number letting users track the progress in real time as well. </li>
              </ul>

              <h3>THE Support Cast</h3>
              <p>Apart from its main responsibilities, this app also comes equipped with various other features and
                  functionalities. These features include the likes of:</p>
              <ul class="new-sec-list">
                  <li>Towed Vehicle Search: Users will now be able to access the towed vehicle database in real time and
                      track the location of their towed vehicle along with other necessary information (as in fine
                      amount, condition of the vehicle, cause, etc). </li>
                  <li>Track My Location: This app allows user to share their location in real time with others. </li>
                  <li> Various other supporting features are planned for next set of releases.</li>
              </ul>
              <h3>Application Development</h3>
              <p>Keeping with Agile methodology, we started this undertaking by aligning 2 separate yet equally
                  important teams to work in tandem.</p>
              <p>The first team along with our UI/UX specialists started with working on visual representation of the
                  project. Detailed and functioning wireframes were delivered to showcase our suggested layouts and
                  application flow. Our focus was on delivering an innovative, user friendly and simple design layout.
              </p>
              <p>The second core development team along with our PM worked on finalizing the overall application
                  architecture with focus on ensuring app is secure, stable and scalable. We looked at real world
                  behaviour to learn/analyse how existing services are used. We let data drive our decision-making when
                  it comes to preparing app architecture.</p>

