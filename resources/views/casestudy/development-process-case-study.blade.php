            <h3>Execution Plan</h3>
            <p>Understanding the elements of a project execution plan provides clarity to the role of the stakeholders
                and helps in overcoming project challenges. For us, the Project Execution Plan (PEP) is the primary
                document that defines how the project will be undertaken. It details the specific activities in the
                project, the resources applied to the project, and the organization of the project. The core elements of
                a project execution<b> plan for this undertaking consists of:</b></p>
            <ul class="new-sec-list">
                <li>Scope Definition: Scope definition entails defining what the project intends to achieve in specific
                    terms.</li>
                <li>Statement of Goals: The statement of goals lists out specific project requirements such as key
                    project deliverables, milestones, and the project life cycle. The goal statement also helps explain.
                </li>
                <li>Why the project is carried out, the purpose served by the project, and the expected benefits from
                    the project.</li>
                <li>The unique challenges that the project will overcome. </li>
                <li>The risks associated with the project and how the project plan will overcome such risks.</li>
                <li><b> Quality and Technical Specifications:</b> While the project scope and statement of goals make
                    clear the output of the project, one important element that needs inclusion in the project execution
                    plan is quality or technical specifications of the work processes and output. The achievement of
                    such stipulated standards determine the successful execution of the project. </li>
                <p><b>The parameters of quality and technical specification include the use of:</b></p>
                <ul class="new-sec-list">
                    <li>Clear and precise definitions for concepts and terms.</li>
                    <li>Measurable Descriptions.</li>
                    <li> Attainable Standards.</li>
                    <li> Time bound definitions.</li>
                    </li>
                </ul>
                <li><b> Allocation of Resources:</b>: The elements of scope, goals, and quality define in clear terms
                    what the project hopes to achieve. The next element of a project execution plan that we take into
                    consideration is the allocation of resources to achieve the stated goals and quality standards.
                    Resources include staff with knowledge or skill sets, monetary constraints, and time consideration.
                </li>
                <li><b> Project Scheduling: </b> Project scheduling by far is the most critical and important element in
                    a PEP. We utilize Gantt Charts that list what will happen and when. This allows us not only break
                    the project into various deliverables, but also allows us resource allocation scheduling more
                    effectively.</li>
            </ul>


