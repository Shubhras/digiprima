              <h3>Background</h3>
              <p>The Swiss company SIGVARIS, 100% family-owned since it was founded, is the global market leader in the
                  manufacture of medical compression garments and celebrated its 150th anniversary in 2014. SIGVARIS
                  produced “rubber-elastic textiles”, which it sold both in Switzerland and abroad.</p>
              <p>The product portfolio was expanded in 2009 when the WELL BEING and SPORTS lines were added to the main
                  MEDICAL line. WELL BEING stockings have a preventive function and relieve the early symptoms of leg
                  conditions. SPORTS products improve athlete performance and recovery time.</p>
              <p>Customers around the world include pharmacies, chemists, orthopedic and medical specialist stores as
                  well as doctors and hospitals. The Group produced over 8 million pairs of stockings in 2016 and
                  generated revenue of around CHF 250 million.</p>
              <h3>Goals: What were the goals of the application? </h3>
              <p>Web Based Financial reporting solution https://www.sigvaris.com in ASP.NET MVC - AngularJS & SQL
                  Server. This is done for a Swiss based manufacturing company and is used for monthly sales-cost data
                  reporting along with using it as a business intelligence solution for decision-making. </p>
              <h3>Strategy: Did the Company try to hire other developers and fail? What were they looking for in a
                  development partner? </h3>
              <p>Yes, company hired other developers and was not getting proper results. Then we got connected. We
                  analyzed the system, and created the Business Analysis document to understand the pain/ need and
                  define the business solution. This document helped us get into an agreement with Sigvaris and to
                  provide a foundation to define what solution needs to do to satisfy the customer's and business'
                  needs.</p>
              <h3>Plan: What did the development process look like, step by step? </h3>
              <h4>Development process and version control:</h4>
              <ul class="new-sec-list">
                  <li> Review, assign task(s).</li>
                  <li> Setup project.</li>
                  <li>Complete task(s), push, commit.</li>
                  <li>Database updates.</li>
                  <li>Link branch to subdomain, testing.</li>
                  <li>Submit and review.</li>
                  <li>Pull requests.</li>
                  <li>Conflicts.</li>
                  <li>Merge with dev and test.</li>
                  <li>Merge with master and confirm updates.</li>
              </ul>
              <h3>Environments</h3>
              <ul class="new-sec-list">
                  <li>Development server - Will be used for development and testing.</li>
                  <li>Staging server - Will be used for client review and UAT.</li>
                  <li>Production server – Final application will be deployed here. </li>
                  
              </ul>
              <h3>Project Management Methodology</h3>
              <ul class="new-sec-list">
                  <p>Agile project management focuses on delivering maximum value against business priorities in the
                      time and budget allowed. </p>
                  <li> The project breaks a requirement into smaller pieces, which are then prioritized by the team in
                      terms of importance.</li>
                  <li>The agile project promotes collaborative working, especially with the customer. </li>
                  <li>The agile project reflects, learns and adjusts at regular intervals to ensure that the customer is
                      always satisfied and is provided with outcomes that result in benefits.</li>
                  <li>Agile methods integrate planning with execution, allowing an organisation to create a working
                      mindset that helps a team respond effectively to changing requirements. </li>
                  <h3>Version Control</h3>
                  <li>Master branch contains the latest STABLE build. Direct push to this branch is forbidden and should
                      not be allowed by Git permissions.</li>
                  <li>Dev branch is the primary development branch for testing before changes and updates are pushed to
                      the master branch. All developers work will be tested on the dev branch and dev subdomain before
                      updates are merged with the master branch.</li>
                  <li>Direct FTP to the server is not allowed. All work will be performed through git.</li>
                  <li> Developers can work on different tasks at the same time.</li>
                  
              </ul>

