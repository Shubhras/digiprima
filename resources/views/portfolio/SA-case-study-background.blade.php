              <h3> Results: How did the application do in production? What ROI did the application provide to the
                  business?</h3>
              <p>Developed an end-to-end solution, where the multiple offices can submit their monthly sales & cost data
                  over the web. Accounting/Controlling department collates the data and run the currency scenarios along
                  with running intercompany profit elimination scenarios. Post which data is send over to SQL DWH and
                  reported on MTD/YTD basis as well as comparison with previous year data.</p>
              <p>This provided them one view of their finance summary and manage the finance better.</p>
