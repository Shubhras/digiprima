            <p>Once the processes were set, it was the time to test. There are numerous ways of user testing, with each
                test depending on the type of software that is to be tested and the data that we intended to derive. The
                leanest and most cost saving format is remote user testing. This allowed for real users (test group) to
                test the system with video and feedback to boot. </p>
            <p>After the tests were done, we compiled the data collected and prepared relevant status reports and
                evaluated the test results. This process helped in determining:</p>
            <ul class="new-sec-list">
                <li>Leave bug and go ahead with the release.</li>
                <li>Push back the release date and fix bugs.</li>
                <li>Add change request for future releases.</li>
            </ul>
            <p>Given the complex nature of software development and human behaviour, we provided tailor-made UAT
                processes that were customized to our client’s specific needs. </p>

